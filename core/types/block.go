// Header represents a block header in the Ethereum blockchain.
type Header struct {
	//指向父区块(parentBlock)的指针。除了创世块(Genesis Block)外，每个区块有且只有一个父区块。
	ParentHash  common.Hash    `json:"parentHash"       gencodec:"required"`
	//Block结构体的成员uncles的RLP哈希值。uncles是一个Header数组，它的存在，颇具匠心。
	UncleHash   common.Hash    `json:"sha3Uncles"       gencodec:"required"`
	//挖掘出这个区块的作者地址。在每次执行交易时系统会给与一定补偿的Ether，这笔金额就是发给这个地址的。
	Coinbase    common.Address `json:"miner"            gencodec:"required"`
	//StateDB中的“state Trie”的根节点的RLP哈希值。Block中每个账户以stateObject对象表示，账户以Address为唯一标示，其信息在相关交易(Transaction)的
	//执行中被修改。所有账户对象可以逐个插入一个Merkle-PatricaTrie(MPT)结构里，形成“state Trie”。
	Root        common.Hash    `json:"stateRoot"        gencodec:"required"`
	//Block中 “tx Trie”的根节点的RLP哈希值。Block的成员变量transactions中所有的tx对象，被逐个插入一个MPT结构，形成“tx Trie”。
	TxHash      common.Hash    `json:"transactionsRoot" gencodec:"required"`
	//Block中的 "Receipt Trie”的根节点的RLP哈希值。Block的所有Transaction执行完后会生成一个Receipt数组，这个数组中的所有Receipt被逐个插入一个
	//MPT结构中，形成"Receipt Trie"。
	ReceiptHash common.Hash    `json:"receiptsRoot"     gencodec:"required"`
	//Bloom过滤器(Filter)，用来快速判断一个参数Log对象是否存在于一组已知的Log集合中。
	Bloom       Bloom          `json:"logsBloom"        gencodec:"required"`
	//区块的难度。Block的Difficulty由共识算法基于parentBlock的Time和Difficulty计算得出，它会应用在区块的‘挖掘’阶段。
	Difficulty  *big.Int       `json:"difficulty"       gencodec:"required"`
	//区块的序号。Block的Number等于其父区块Number +1。
	Number      *big.Int       `json:"number"           gencodec:"required"`
	//区块内所有Gas消耗的理论上限。该数值在区块创建时设置，与父区块有关。具体来说，根据父区块的GasUsed同GasLimit * 2/3的大小关系来计算得出。
	GasLimit    uint64         `json:"gasLimit"         gencodec:"required"`
	//区块内所有Transaction执行时所实际消耗的Gas总和
	GasUsed     uint64         `json:"gasUsed"          gencodec:"required"`
	//区块“应该”被创建的时间。由共识算法确定，一般来说，要么等于parentBlock.Time + 10s，要么等于当前系统时间。
	Time        *big.Int       `json:"timestamp"        gencodec:"required"`
	Extra       []byte         `json:"extraData"        gencodec:"required"`
	MixDigest   common.Hash    `json:"mixHash"          gencodec:"required"`
	//一个64bit的哈希数，它被应用在区块的"挖掘"阶段，并且在使用中会被修改。
	Nonce       BlockNonce     `json:"nonce"            gencodec:"required"`
}

// field type overrides for gencodec
type headerMarshaling struct {
	Difficulty *hexutil.Big
	Number     *hexutil.Big
	GasLimit   hexutil.Uint64
	GasUsed    hexutil.Uint64
	Time       *hexutil.Big
	Extra      hexutil.Bytes
	Hash       common.Hash `json:"hash"` // adds call to Hash() in MarshalJSON
}

// Hash returns the block hash of the header, which is simply the keccak256 hash of its
// RLP encoding.
func (h *Header) Hash() common.Hash {
	return rlpHash(h)
}

// HashNoNonce returns the hash which is used as input for the proof-of-work search.
func (h *Header) HashNoNonce() common.Hash {
	return rlpHash([]interface{}{
		h.ParentHash,
		h.UncleHash,
		h.Coinbase,
		h.Root,
		h.TxHash,
		h.ReceiptHash,
		h.Bloom,
		h.Difficulty,
		h.Number,
		h.GasLimit,
		h.GasUsed,
		h.Time,
		h.Extra,
	})
}

// Size returns the approximate memory used by all internal contents. It is used
// to approximate and limit the memory consumption of various caches.
func (h *Header) Size() common.StorageSize {
	return common.StorageSize(unsafe.Sizeof(*h)) + common.StorageSize(len(h.Extra)+(h.Difficulty.BitLen()+h.Number.BitLen()+h.Time.BitLen())/8)
}

func rlpHash(x interface{}) (h common.Hash) {
	hw := sha3.NewKeccak256()
	rlp.Encode(hw, x)
	hw.Sum(h[:0])
	return h
}

// Body is a simple (mutable, non-safe) data container for storing and moving
// a block's data contents (transactions and uncles) together.
type Body struct {
	Transactions []*Transaction
	Uncles       []*Header
}

// Block represents an entire block in the Ethereum blockchain.
type Block struct {    //区块
	header       *Header
	uncles       []*Header
	transactions Transactions   //交易对象的列表

	// caches
	hash atomic.Value
	size atomic.Value

	// Td is used by package core to store the total difficulty 整个区块链表从源头创世块开始，到当前区块截止，累积的所有区块Difficulty之
	// of the chain up to and including the block.              和，td取名totalDifficulty
	td *big.Int                //挖矿难度

	// These fields are used by package eth to track
	// inter-peer block relay.
	ReceivedAt   time.Time
	ReceivedFrom interface{}
}

// DeprecatedTd is an old relic for extracting the TD of a block. It is in the
// code solely to facilitate upgrading the database from the old format to the
// new, after which it should be deleted. Do not use!
func (b *Block) DeprecatedTd() *big.Int {
	return b.td
}

